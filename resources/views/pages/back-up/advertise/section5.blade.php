<script>
    $('#carousel5').on('slide.bs.carousel', function () {
        $(this).find('.carousel-item.active .content-sec-5').removeClass('active').hide();
        $(this).find('.carousel-item:not(.active) .content-sec-5').addClass('active').show();
    });


    $('#carousel5').on('slide.bs.carousel', function (e) {
        let activeItemId = e.relatedTarget.id;

        $('.circle-5').removeClass('green-bg');

        $('.circle-5').each(function () {
            var circleId = $(this).attr('id');

            if (circleId === activeItemId) {
                $(this).addClass('green-bg');
            }
        });
    });
</script>
